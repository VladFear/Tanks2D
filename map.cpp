Map::~Map()
{
	SDL_DestroyTexture(ground);
	SDL_DestroyTexture(wall);
}

void Map::loadMap(const int lvl[ROWS][COLUMNS])
{
	for (int i = 0; i < ROWS; i++)
		for (int j = 0; j < COLUMNS; j++)
			tiles[i][j] = lvl[i][j];
}

void Map::drawMap()
{
	for (int i = 0; i < ROWS; i++)
		for (int j = 0; j < COLUMNS; j++)
		{
			dest.x = j * dest.w;
			dest.y = i * dest.h;

			switch (tiles[i][j])
			{
				case static_cast<int>(Texture::GROUND):
					TextureManager::drawTexture(ground, src, dest);
					break;

				case static_cast<int>(Texture::WALL):
					TextureManager::drawTexture(wall, src, dest);
					break;

				default:
					break;
			}
		}
}
