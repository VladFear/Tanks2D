Map::Map()
{
	wall = TextureManager::loadTexture("../Tanks2D/images/wall1.png");
	loadMap(lvl);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::loadMap(int lvl[20][25])
{
	for (int i = 0; i < 20; i++)
		for (int j = 0; j < 25; j++)
			tiles[i][j] = lvl[i][j];
}

void Map::drawMap()
{
	for (int i = 0; i < 20; i++)
		for (int j = 0; j < 25; j++)
		{
			dest.x = j * dest.w;
			dest.y = i * dest.h;

			switch (tiles[i][j])
			{
				case 1:
					TextureManager::drawTexture(wall, src, dest);
					break;

				default:
					break;
			}
		}
}
